    public boolean validar() {
        Object[] datos = {
        TxtfClaveProducto.getText(),
        TxtfNombreProducto.getText(),
        TxtfDescripcion.getText(),
        TxtfHechoEn.getText(),
        TxtfPrecioCompra.getText(),
        TxtfPrecioVenta.getText(),
        TxtfCompañiaFabricacion.getText()
        };
        boolean result = true;
        
        for (int i = 0; i <= datos.length - 1; i++) {
            if (datos[i].toString().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Algunos campos estan vacios");
                result = false;
                break;
            }
        }
        
        return result;
    }
    
    
    private void btnGuardadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardadActionPerformed
        Producto producto = null;
        if(!validar())return;
        
        try {
            producto = new Producto(
                    TxtfClaveProducto.getText(),
                    TxtfNombreProducto.getText(),
                    TxtfDescripcion.getText(),
                    TxtfHechoEn.getText(),
                    Double.parseDouble(TxtfPrecioCompra.getText()),
                    Double.parseDouble(TxtfPrecioVenta.getText()),
                    TxtfCompañiaFabricacion.getText());
        } catch (Exception e) {
            System.out.println(e);
            producto = null;
        }

        operaciones.CrearProducto(producto);
    }//GEN-LAST:event_btnGuardadActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        productos = operaciones.getProductos();

        for (Producto producto : productos) {
            if (producto.getClaveProducto().equals(TxtfClaveProducto.getText())) {

                operaciones.BorrarProductoByClave(TxtfClaveProducto.getText());
                System.out.println("Registro encontrado");
                return;
            }
        }

        System.out.println("Registro no encontrado");

        productos = operaciones.getProductos();
    }//GEN-LAST:event_btnEliminarActionPerformed

