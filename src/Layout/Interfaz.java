    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        if(!validar())return;
        
        Producto producto = null;
        try {
            producto = new Producto(
                    TxtfClaveProducto.getText(),
                    TxtfNombreProducto.getText(),
                    TxtfDescripcion.getText(),
                    TxtfHechoEn.getText(),
                    Double.parseDouble(TxtfPrecioCompra.getText()),
                    Double.parseDouble(TxtfPrecioVenta.getText()),
                    TxtfCompañiaFabricacion.getText());
        } catch (Exception e) {
            System.out.println("Error desde la interfaz"+e);
            producto = null;
        }
        if(producto != null){
            operaciones.ActualizarProductoByClave(producto);
        }else{
            System.out.println("Producto no valido");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

